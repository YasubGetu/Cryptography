    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jComboBox1.getSelectedItem() == "OTP"){
        plainText = "";
 
        int plain[] = new int[decryptionKey.length()];

        for (int i = 0; i < decryptionKey.length(); i++) {
            plain[i]
                = messageToDecrypt.charAt(i) - 'A'
                  - (decryptionKey.charAt(i) - 'A');
        }
 
        for (int i = 0; i < decryptionKey.length(); i++) {
            if (plain[i] < 0) {
                plain[i] = plain[i] + 26;
            }
        }
 
        for (int i = 0; i < decryptionKey.length(); i++) {
            int x = plain[i] + 'A';
            plainText += (char)x;
        }
        }
        else if(jComboBox1.getSelectedItem() == "3DES"){
            plainText = decrypt3DES(Base64.getDecoder().decode(messageToDecrypt));
        }
        else{
            plainText = decryptAES(messageToDecrypt);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

